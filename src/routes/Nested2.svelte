<p>
	The country's refusal to cooperate with the EU does not mean it resists the broader
	trend toward restrictive migration governance—this stands in contrast to its claim
	of African solidarity. Even without formal agreements, Algeria has developed
	practices that mirror the logic of "Fortress Europe": containment, deterrence, and
	expulsion, even before the EU began to externalize its borders.
</p>
