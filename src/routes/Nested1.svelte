	Here, numbers remain snapshots rather than a full picture. So I went looking for the traces.
	This Data Bit is based on a dataset scraped from weekly operational reports published by the
	<a
		href="https://www.mdn.dz/site_principal/sommaire/archives/archives_actualites_fr.php"
		target="_blank"
		rel="noopener noreferrer"
	>
		Algerian Ministry of Defence
	</a>.
	These reports are one of the few official sources that regularly mention migration-related arrests. Using web scraping
	techniques in R, I collected more than 100 reports spanning 2023 to 2025 and complemented
	them with older reports hidden in archived local news articles. Together with interviews taken during fieldwork in Tunis in early 2026, this dataset provides a rare longitudinal view of Algeria’s migration enforcement practices.
